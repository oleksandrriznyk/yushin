package ua.library.dao.entity;

public class Genre extends Entity {

	private static final long serialVersionUID = -2663825739218332399L;
	int id;
	String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
